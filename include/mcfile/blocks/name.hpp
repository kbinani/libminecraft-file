static inline std::optional<Characteristics> BlockCharacteristic(BlockId id) {
    static std::vector<Characteristics> const table(CreateTable());
    uint32_t i = static_cast<uint32_t>(id);
    if (i < 1) {
        return std::nullopt;
    }
    uint32_t index = i - 1;
    if (table.size() <= index) {
        return std::nullopt;
    }
    return table[index];
}

