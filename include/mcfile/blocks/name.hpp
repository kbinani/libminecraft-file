static inline std::u8string Name(BlockId id, int dataVersion) {
    if (dataVersion > 0) {
        if (id == minecraft::dirt_path && dataVersion < 2724) {
            return u8"minecraft:grass_path";
        } else if (id == minecraft::water_cauldron && dataVersion < 2724) {
            return u8"minecraft:cauldron";
        } else if (id == minecraft::short_grass && dataVersion < 3698) {
            return u8"minecraft:grass";
        }
    }
    auto characteristic = impl::BlockCharacteristic(id);
    if (!characteristic) {
        return u8"";
    }
    return characteristic->name;
}

static inline bool IsTransparent(BlockId id) {
    auto characteristic = impl::BlockCharacteristic(id);
    if (!characteristic) {
        return false;
    }
    return !characteristic->opaque;
}

